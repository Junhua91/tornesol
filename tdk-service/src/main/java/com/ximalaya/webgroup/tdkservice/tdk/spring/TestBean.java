package com.ximalaya.webgroup.tdkservice.tdk.spring;

public class TestBean {

    private int a;
    private int b;

    public TestBean() {
    }

    public TestBean(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
