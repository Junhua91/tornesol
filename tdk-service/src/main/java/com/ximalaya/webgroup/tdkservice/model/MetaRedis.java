package com.ximalaya.webgroup.tdkservice.model;

public class MetaRedis {

    private String title;
    private String keyword;
    private String description;
    private String tips;
    private String uniqueCodes;
    private String recommendation;
    private String friendLink;
    private String pageConfig;
    private String hotLink;
    private String metaStyle;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getTips() {
        return tips;
    }

    public void setTips(String tips) {
        this.tips = tips;
    }

    public String getUniqueCodes() {
        return uniqueCodes;
    }

    public void setUniqueCodes(String uniqueCodes) {
        this.uniqueCodes = uniqueCodes;
    }

    public String getRecommendation() {
        return recommendation;
    }

    public void setRecommendation(String recommendation) {
        this.recommendation = recommendation;
    }

    public String getFriendLink() {
        return friendLink;
    }

    public void setFriendLink(String friendLink) {
        this.friendLink = friendLink;
    }

    public String getPageConfig() {
        return pageConfig;
    }

    public void setPageConfig(String pageConfig) {
        this.pageConfig = pageConfig;
    }

    public String getMetaStyle() {
        return metaStyle;
    }

    public void setMetaStyle(String metaStyle) {
        this.metaStyle = metaStyle;
    }

    public String getHotLink() {
        return hotLink;
    }

    public void setHotLink(String hotLink) {
        this.hotLink = hotLink;
    }

}
